'use client';

import { memo, useMemo } from 'react';
import { motion } from 'framer-motion';
import Image from 'next/image';

const EducationTimelineItem = memo(function EducationTimelineItem({ item, index, isLast, totalItems }) {
  // This component now only handles education items
  const {
    institution,
    studyType,
    area,
    startDate,
    endDate,
    gpa,
    courses = [],
    description,
    location,
  } = item;

  // Calculate colors for this timeline item (education only)
  const educationColors = [
    '#3b82f6, #1d4ed8', // blue gradient
    '#10b981, #059669', // emerald gradient
    '#8b5cf6, #7c3aed', // violet gradient
    '#f59e0b, #d97706', // amber gradient
    '#ef4444, #dc2626', // red gradient
    '#06b6d4, #0891b2', // cyan gradient
  ];
  
  const colorGradient = educationColors[index % educationColors.length];
  
  const iconColors = [
    'bg-gradient-to-br from-blue-500 to-blue-700',
    'bg-gradient-to-br from-emerald-500 to-emerald-700',
    'bg-gradient-to-br from-violet-500 to-violet-700',
    'bg-gradient-to-br from-amber-500 to-amber-700',
    'bg-gradient-to-br from-red-500 to-red-700',
    'bg-gradient-to-br from-cyan-500 to-cyan-700',
    'bg-gradient-to-br from-pink-500 to-pink-700',
    'bg-gradient-to-br from-orange-500 to-orange-700',
    'bg-gradient-to-br from-lime-500 to-lime-700',
    'bg-gradient-to-br from-indigo-500 to-indigo-700',
    'bg-gradient-to-br from-teal-500 to-teal-700',
    'bg-gradient-to-br from-purple-500 to-purple-700',
  ];
  
  const iconColorClass = iconColors[index % iconColors.length];

  // Format dates for education
  const formattedDates = useMemo(() => {
    const start = startDate ? new Date(startDate) : null;
    const end = endDate ? new Date(endDate) : null;
    
    const formatDate = (date) => {
      if (!date) return 'Not specified';
      return date.toLocaleDateString('en-US', { 
        year: 'numeric', 
        month: 'short' 
      });
    };

    const startStr = formatDate(start);
    const endStr = end ? formatDate(end) : 'Present';
    
    return {
      range: `${startStr} - ${endStr}`,
    };
  }, [startDate, endDate]);

  // Animation variants
  const itemVariants = {
    hidden: { opacity: 0, y: 50 },
    visible: (i) => ({
      opacity: 1,
      y: 0,
      transition: {
        delay: i * 0.1,
        duration: 0.6,
        ease: "easeOut"
      }
    })
  };

  // Determine timeline side for vertical layout (alternating)
  const isEven = index % 2 === 0;
  const timelineStart = isEven ? 'timeline-start md:text-end' : 'timeline-end';
  const timelineEnd = isEven ? 'timeline-end' : 'timeline-start md:text-start';

  // Education icon
  const getIcon = () => {
    return (
      <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
        <path d="M10.394 2.08a1 1 0 00-.788 0l-7 3a1 1 0 000 1.84L5.25 8.051a.999.999 0 01.356-.257l4-1.714a1 1 0 11.788 1.838L7.667 9.088l1.94.831a1 1 0 00.787 0l7-3a1 1 0 000-1.838l-7-3zM3.31 9.397L5 10.12v4.102a8.969 8.969 0 00-1.05-.174 1 1 0 01-.89-.89 11.115 11.115 0 01.25-3.762zM9.3 16.573A9.026 9.026 0 007 14.935v-3.957l1.818.78a3 3 0 002.364 0l5.508-2.361a11.026 11.026 0 01.25 3.762 1 1 0 01-.89.89 8.968 8.968 0 00-5.35 2.524 1 1 0 01-1.4 0zM6 18a1 1 0 001-1v-2.065a8.935 8.935 0 00-2-.712V17a1 1 0 001 1z"/>
      </svg>
    );
  };

  return (
    <motion.li 
      className="timeline-item"
      initial="hidden"
      animate="visible"
      variants={itemVariants}
      custom={index}
    >
      {/* Timeline line before (except for first item) */}
      {index > 0 && (
        <hr 
          style={{
            background: `linear-gradient(to right, ${colorGradient})`,
          }}
        />
      )}

      {/* Institution Info */}
      <div className={`${timelineStart} mb-10`}>
        <div className="text-lg font-bold text-base-content mb-2 text-center md:text-right">
          {institution}
        </div>
        {location && (
          <div className="text-sm text-base-content/60 text-center md:text-right">
            {location}
          </div>
        )}
        {formattedDates.range && (
          <div className="text-sm text-primary font-semibold text-center md:text-right mt-1">
            {formattedDates.range}
          </div>
        )}
      </div>

      {/* Timeline Connector */}
      <div className="timeline-middle">
        <div className={`w-10 h-10 rounded-full flex items-center justify-center text-white shadow-lg ${iconColorClass}`}>
          {getIcon()}
        </div>
      </div>

      {/* Education Details */}
      <div className={`${timelineEnd} mb-10`}>
        <div className="timeline-box bg-base-200 shadow-lg">
          <div className="p-6">
            {/* Title */}
            <div className="mb-4">
              <h3 className="card-title text-xl text-primary mb-2">
                {`${studyType} in ${area}`}
              </h3>
              <div className="flex flex-wrap gap-2 text-sm">
                <div className="badge badge-info badge-outline">
                  Education
                </div>
                {gpa && (
                  <div className="badge badge-primary badge-outline">
                    GPA: {gpa}
                  </div>
                )}
              </div>
            </div>

            {/* Description */}
            {description && (
              <div className="mb-4">
                <p className="text-base-content/80 leading-relaxed">
                  {description}
                </p>
              </div>
            )}

            {/* Courses */}
            {courses.length > 0 && (
              <div className="mb-4">
                <h4 className="font-semibold text-base-content mb-3">
                  Key Courses
                </h4>
                <div className="flex flex-wrap gap-2">
                  {courses.slice(0, 6).map((course, courseIndex) => (
                    <span key={courseIndex} className="badge badge-outline">
                      {course}
                    </span>
                  ))}
                  {courses.length > 6 && (
                    <span className="badge badge-outline">
                      +{courses.length - 6} more
                    </span>
                  )}
                </div>
              </div>
            )}
          </div>
        </div>
      </div>

      {/* Timeline line after (except for last item) */}
      {!isLast && (
        <hr 
          style={{
            background: `linear-gradient(to right, ${colorGradient})`,
          }}
        />
      )}
    </motion.li>
  );
});

export default EducationTimelineItem;